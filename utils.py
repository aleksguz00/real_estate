def clean_text(text: str) -> str:
    """
    Убрать из текста:
    - Хэштеги
    - «Для записи на просмотр: WhatsApp | Telegram»
    - Ссылки «Продажа/Аренда квартир»
    - Код объекта в конце
    - Лишние пустые строки
    Буллеты (строки начинающиеся с •) сохраняются полностью.
    """
    # Собираем буллеты до очистки — восстановим если что-то срежется
    bullet_lines = [ln for ln in text.split("\n") if ln.strip().startswith("•")]

    # Убираем хэштеги
    text = re.sub(r"#\S+", "", text)

    # Убираем «Для записи на просмотр» и всё после
    text = re.sub(r"для записи на просмотр.*", "", text, flags=re.IGNORECASE | re.DOTALL)

    # Убираем рекламные строки канала — только саму строку (MULTILINE, без DOTALL)
    # DOTALL здесь нельзя: "аренда квартиры:" в заголовке тоже матчится
    text = re.sub(r"^(аренда|продажа)\s+квартир[^\n]*$", "", text, flags=re.IGNORECASE | re.MULTILINE)
    text = re.sub(r"^аренда коммерческих[^\n]*$", "", text, flags=re.IGNORECASE | re.MULTILINE)

    # Убираем код объекта в конце (например 2336АК)
    text = re.sub(r"\b\d*[А-ЯA-Z]{2,3}\s*$", "", text.strip(), flags=re.MULTILINE)

    # Убираем «Сдано!» / «Продано!» в начале
    text = re.sub(r"^(сдано|продано|снято)[!.]?\s*\n?", "", text.strip(), flags=re.IGNORECASE)

    # Убираем лишние пустые строки
    text = re.sub(r"\n{3,}", "\n\n", text)

    # Восстанавливаем буллеты, которые могли срезаться
    surviving = {ln.strip() for ln in text.split("\n") if ln.strip().startswith("•")}
    missing = [ln for ln in bullet_lines if ln.strip() not in surviving]
    if missing:
        text = text.rstrip() + "\n" + "\n".join(missing)

    return text.strip()


