def extract_area_and_floor(text: str) -> tuple[int | None, int | None, int | None, int | None]:
    """
    Возвращает (area, floor, floors_total, area_land).
    """
    clean = strip_markdown(text)
    area = None
    floor = None
    floors_total = None
    area_land = None

    # Площадь: "Площадь: 78 м²" или "Площадь дома: 400 м²"
    # Ищем именно площадь дома/квартиры, не участка
    area_match = re.search(r"площадь(?:\s+(?:дома|квартиры|помещения))?\s*:\s*(\d[\d\s]*)\s*м", clean, re.IGNORECASE)
    if area_match:
        try:
            area = int(area_match.group(1).replace(" ", ""))
        except ValueError:
            pass

    # Участок: "Участок: 160 м²" или "/ Участок: 40 м²"
    land_match = re.search(r"участок\s*:\s*(\d[\d\s]*)\s*м", clean, re.IGNORECASE)
    if land_match:
        try:
            area_land = int(land_match.group(1).replace(" ", ""))
        except ValueError:
            pass

    # Этаж: "Этаж: 27/35" или "Этаж: 15" или "Этажность: 2"
    floor_match = re.search(r"этаж(?:ность)?\s*:\s*(\d+)\s*(?:/\s*(\d+))?", clean, re.IGNORECASE)
    if floor_match:
        try:
            floor = int(floor_match.group(1))
            if floor_match.group(2):
                floors_total = int(floor_match.group(2))
        except ValueError:
            pass
    else:
        # Этаж в скобках в первой строке: "(3 этаж)"
        bracket_match = re.search(r"\((\d+)\s*этаж\)", clean, re.IGNORECASE)
        if bracket_match:
            try:
                floor = int(bracket_match.group(1))
            except ValueError:
                pass

    return area, floor, floors_total, area_land


