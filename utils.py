def extract_features_and_heating(text: str) -> tuple[list[str], list[str]]:
    """Извлечь список удобств и тип отопления из текста."""
    text_lower = text.lower()

    heating = []
    for keyword, value in HEATING_KEYWORDS.items():
        if keyword in text_lower and value not in heating:
            heating.append(value)

    features = []
    for keyword, value in FEATURES_KEYWORDS.items():
        if keyword in text_lower and value not in features:
            features.append(value)

    return features, heating


