def find_district_by_street(street: str | None, address: str | None) -> str | None:
    """Определить район по названию улицы с нечётким поиском."""
    text = ((street or "") + " " + (address or "")).lower()
    text = re.sub(r'\bул\.\s*', 'улица ', text)
    text = re.sub(r'\bпросп\.\s*', 'проспект ', text)
    text = re.sub(r'\bпр-т\.?\s*', 'проспект ', text)
    text = re.sub(r'\bпр\.\s*', 'проспект ', text)
    text = re.sub(r'\bтуп\.\s*', 'тупик ', text)
    text = re.sub(r'\bпер\.\s*', 'переулок ', text)
    text = re.sub(r'\bш\.\s*', 'шоссе ', text)

    # Точное совпадение
    for keyword, district in STREET_TO_DISTRICT.items():
        if keyword in text:
            return district

    # Нечёткий поиск через rapidfuzz
    from rapidfuzz import process, fuzz
    result = process.extractOne(
        text,
        STREET_TO_DISTRICT.keys(),
        scorer=fuzz.partial_ratio,
        score_cutoff=85,
    )
    if result:
        return STREET_TO_DISTRICT[result[0]]

    return None


