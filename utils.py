def extract_address(text: str) -> str | None:
    """Извлечь адрес из строки 'Адрес: ...'"""
    # Убираем markdown перед поиском
    clean = strip_markdown(text)
    match = re.search(r"адрес\s*:\s*(.+)", clean, re.IGNORECASE)
    if not match:
        return None

    address = match.group(1).strip()

    # Убираем уточнения в скобках: (ЖК Rainbow), (Здание В) и т.д.
    address = re.sub(r"\s*\([^)]*\)", "", address).strip()

    # Убираем лишние символы в конце
    address = address.rstrip(".,;")

    # Если адрес на следующей строке — берём только первую строку
    address = address.split("\n")[0].strip()

    for alias, display in ADDRESS_DISPLAY_ALIASES.items():
        if address and alias.lower() in address.lower():
            address = re.sub(re.escape(alias), display, address, flags=re.IGNORECASE)

    return address if address else None


