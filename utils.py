def detect_deal_and_type(text: str) -> tuple[str, str]:
    """
    Возвращает (deal_type, property_type).
    deal_type: rent / sale
    property_type: apartment / studio / house / commercial
    """
    first_line = text.strip().split("\n")[0].lower()

    # Тип сделки
    if "продает" in first_line or "продаёт" in first_line:
        deal_type = "sale"
    else:
        deal_type = "rent"

    # Тип объекта
    if "студия" in first_line:
        property_type = "studio"
    elif "частный дом" in first_line or "квартира в частном доме" in first_line:
        property_type = "house"
    elif "дом" in first_line:
        property_type = "house"
    elif "коммерческ" in first_line:
        property_type = "commercial"
    elif "офис" in first_line:
        property_type = "commercial"
    elif "склад" in first_line:
        property_type = "commercial"
    else:
        property_type = "apartment"

    return deal_type, property_type


def detect_rooms(text: str) -> str | None:
    """Извлечь количество комнат из первой строки."""
    first_line = text.strip().split("\n")[0].lower()

    if "студия" in first_line:
        return "Студия"

    match = re.search(r"(\d)\s*\+\s*1", first_line)
    if match:
        return f"{match.group(1)}+1"

    return None


def detect_source_code(text: str) -> str | None:
    """Извлечь код объекта (например 2336АК или 554РК)."""
    # Ищем код в конце строк с хэштегами или в конце текста
    match = re.search(r"\b(\d*[А-ЯA-Z]{2,3})\s*$", text.strip(), re.MULTILINE)
    if match:
        code = match.group(1)
        # Код должен содержать буквы АК или РК
        if re.search(r"[АA][КK]|[РP][КK]", code, re.IGNORECASE):
            return code
    return None


