def detect_deal_and_type(text: str) -> tuple[str, str]:
    """
    Возвращает (deal_type, property_type).
    deal_type: rent / sale
    property_type: apartment / studio / house / commercial
    """
    first_line = text.strip().split("\n")[0].lower()

    # Тип сделки
    if "продает" in first_line or "продаёт" in first_line:
        deal_type = "sale"
    else:
        deal_type = "rent"

    # Тип объекта
    if re.search(r"[cс]туди[яиью]", first_line):
        property_type = "studio"
    elif "частный дом" in first_line or "квартира в частном доме" in first_line:
        property_type = "house"
    elif "дом" in first_line and "квартира" not in first_line:
        property_type = "house"
    elif any(w in first_line for w in ["отель", "гостиниц", "хостел"]):
        property_type = "commercial"
    elif "коммерческ" in first_line or "торговая" in first_line:
        property_type = "commercial"
    elif "офис" in first_line:
        property_type = "commercial"
    elif "склад" in first_line:
        property_type = "commercial"
    else:
        property_type = "apartment"

    return deal_type, property_type


def detect_rooms(text: str) -> str | None:
    """Извлечь количество комнат из первых 5 строк."""
    header = "\n".join(text.strip().split("\n")[:5]).lower()

    if re.search(r"[cс]туди[яиью]", header):
        return "Студия"

    # 2+1, 3+1, 4+1
    match = re.search(r"(\d)\s*\+\s*1", header)
    if match:
        return f"{match.group(1)}+1"

    # 1-комнатная, 2-комнатная, 1к, 2-к
    match = re.search(r"(\d)[\s-]?(?:комнатн|к(?:\b|в))", header)
    if match:
        return f"{match.group(1)}-комн."

    # словами
    word_map = {
        "однокомнатн": "1-комн.", "двухкомнатн": "2-комн.",
        "трёхкомнатн": "3-комн.", "трехкомнатн": "3-комн.",
        "четырёхкомнатн": "4-комн.", "четырехкомнатн": "4-комн.",
    }
    for kw, val in word_map.items():
        if kw in header:
            return val

    # "Комнат: 5"
    match = re.search(r"комнат\s*:\s*(\d+)", header)
    if match:
        return f"{match.group(1)}-комн."

    return None


def detect_source_code(text: str) -> str | None:
    """Извлечь код объекта (например 2336АК или 554РК)."""
    # Ищем код в конце строк с хэштегами или в конце текста
    match = re.search(r"\b(\d*[А-ЯA-Z]{2,3})\s*$", text.strip(), re.MULTILINE)
    if match:
        code = match.group(1)
        # Код должен содержать буквы АК или РК
        if re.search(r"[АA][КK]|[РP][КK]", code, re.IGNORECASE):
            return code
    return None


