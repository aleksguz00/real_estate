async def parse_post(text: str) -> dict | None:
    """
    Извлечь все данные из текста объявления.
    Возвращает словарь с данными или None если пост неактуален/не является объявлением.
    """
    if not text:
        return None

    # Проверяем ключевые слова закрытой сделки ДО проверки длины
    # (админ может оставить только "Сдано!" — 7 символов)
    _check = text.strip().lower()
    if any(word in _check[:100] for word in ["сдано", "продано", "снято", "сдан", "продан"]):
        return {"is_active": False}

    if len(text) < 30:
        return None

    # Убираем markdown форматирование
    text = strip_markdown(text)

    # Фильтруем навигационные / рекламные посты без цены и глагола сделки
    header5 = "\n".join(text.strip().split("\n")[:5]).lower()
    has_deal_verb = any(w in header5 for w in ["сдается", "продается", "сдаётся", "продаётся", "аренда", "продажа"])
    has_price = "$" in text
    if not has_deal_verb and not has_price:
        return None

    # Извлекаем все данные
    deal_type, property_type = detect_deal_and_type(text)
    rooms = detect_rooms(text)
    source_code = detect_source_code(text)
    price, price_season, deposit = extract_price(text)
    address = extract_address(text)

    # Фильтруем объекты из пригородов — они не входят в зону работы
    EXCLUDED_LOCATIONS = ["кобулети", "чакви", "гонио", "квариати", "сарпи", "батуми силк роуд"]
    if address:
        addr_lower = address.lower()
        if any(loc in addr_lower for loc in EXCLUDED_LOCATIONS):
            return None
    text_lower = text.lower()
    if any(loc in text_lower[:200] for loc in ["кобулети", "чакви"]):
        return None

    area, floor, floors_total, area_land = extract_area_and_floor(text)
    features, heating = extract_features_and_heating(text)
    clean = clean_text(text)

    # Определяем район через Яндекс Геокодер
    district, lat, lon = await geocode_address(address) if address else (None, None, None)

    return {
        "deal_type":     deal_type,
        "property_type": property_type,
        "rooms":         rooms,
        "source_code":   source_code,
        "price":         price,
        "price_season":  price_season,
        "deposit":       deposit,
        "address":       address,
        "district":      district,
        "lat":           lat,
        "lon":           lon,
        "area":          area,
        "area_land":     area_land,
        "floor":         floor,
        "floors_total":  floors_total,
        "features":      features,
        "heating":       heating,
        "text":          clean,
        "is_active":     True,
    }
