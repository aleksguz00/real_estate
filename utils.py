def extract_price(text: str) -> tuple[int | None, int | None, int | None]:
    """
    Возвращает (price, price_season, deposit).
    """
    lines = text.strip().split("\n")

    price = None
    price_season = None
    deposit = None

    for line in lines[:8]:
        line_clean = line.strip()

        # Депозит: "Депозит - 300 $" или "Депозит: 300$"
        dep_match = re.search(r"депозит\s*[-–:]?\s*([\d\s]+)\s*\$", line_clean, re.IGNORECASE)
        if dep_match:
            deposit = int(dep_match.group(1).replace(" ", ""))
            continue

        # Цена в сезон: "В сезон - 400 $" или "Цена в сезон - 700 $"
        season_match = re.search(r"в\s+сезон\s*[-–:]?\s*([\d\s]+)\s*\$", line_clean, re.IGNORECASE)
        if season_match:
            price_season = int(season_match.group(1).replace(" ", ""))
            continue

        # Основная цена: "420 000 $" или "420000$" или "400 $"
        price_match = re.search(r"^(\d[\d\s]{0,8})\s*\$", line_clean)
        if price_match and price is None:
            try:
                price = int(price_match.group(1).replace(" ", ""))
            except ValueError:
                pass

    return price, price_season, deposit


