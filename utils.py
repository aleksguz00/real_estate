    text = (street or "") + " " + (address or "")
    text_lower = text.lower()
    # Убираем сокращения
    text_lower = re.sub(r'\bул\.\s*|\bпросп\.\s*|\bпер\.\s*', '', text_lower)

    # Точное совпадение
    for keyword, district in STREET_TO_DISTRICT.items():
        if keyword in text_lower:
            return district

    # Нечёткое совпадение через rapidfuzz
    try:
        from rapidfuzz import fuzz
        best_match = None
        best_score = 0
        for keyword, district in STREET_TO_DISTRICT.items():
            score = fuzz.partial_ratio(keyword, text_lower)
            if score > best_score and score >= 85:
                best_score = score
                best_match = district
        if best_match:
            return best_match
    except ImportError:
        pass

    return None


