async def geocode_address(address: str) -> tuple[str | None, float | None, float | None]:
    """
    Отправить адрес в Яндекс Геокодер.
    Возвращает (district, lat, lon).
    """
    if not address or not YANDEX_GEOCODER_KEY:
        return None, None, None

    full_address = f"Батуми, {address}"

    url = "https://geocode-maps.yandex.ru/1.x/"
    params = {
        "apikey": YANDEX_GEOCODER_KEY,
        "geocode": full_address,
        "format": "json",
        "lang": "ru_RU",
        "results": 1,
    }

    try:
        async with aiohttp.ClientSession() as session:
            async with session.get(url, params=params, timeout=aiohttp.ClientTimeout(total=10)) as resp:
                if resp.status != 200:
                    return find_district_by_street(None, address), None, None
                data = await resp.json()

        collection = data["response"]["GeoObjectCollection"]
        members = collection.get("featureMember", [])
        if not members:
            return find_district_by_street(None, address), None, None

        geo = members[0]["GeoObject"]

        # Координаты
        pos = geo["Point"]["pos"].split()
        lon = float(pos[0])
        lat = float(pos[1])

        # Извлекаем название улицы из компонентов
        street = None
        components = geo.get("metaDataProperty", {}) \
                        .get("GeocoderMetaData", {}) \
                        .get("Address", {}) \
                        .get("Components", [])

        for comp in components:
            if comp.get("kind") == "street":
                street = comp.get("name")
                break

        # Определяем район по улице
        district = find_district_by_street(street, address)

        return district, lat, lon

    except Exception as e:
        print(f"[Геокодер] Ошибка: {e}")
        # Fallback — определяем район только по адресу без геокодера
        return find_district_by_street(None, address), None, None


